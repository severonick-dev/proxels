const DOCS: LegalSeed[] = [
  {
    slug: LegalDocSlug.privacy,
    title: 'Политика конфиденциальности',
    version: CONSENT_VERSIONS.privacy,
    contentMd: PRIVACY_MD,
  },
  {
    slug: LegalDocSlug.offer,
    title: 'Публичная оферта',
    version: CONSENT_VERSIONS.offer,
    contentMd: OFFER_MD,
  },
  {
    slug: LegalDocSlug.cookie,
    title: 'Использование cookie',
    version: CONSENT_VERSIONS.cookie,
    contentMd: COOKIE_MD,
  },
];

export async function seedLegalDocs(prisma: PrismaClient): Promise<void> {
  for (const doc of DOCS) {
    const existing = await prisma.legalDoc.findUnique({
      where: { slug_version: { slug: doc.slug, version: doc.version } },
    });
    if (existing) {
      await prisma.legalDoc.update({
        where: { id: existing.id },
        data: {
          title: doc.title,
          contentMd: doc.contentMd,
          publishedAt: existing.publishedAt ?? new Date(),
        },
      });
      console.log(`  ↻ legal "${doc.slug}@${doc.version}" updated`);
    } else {
      await prisma.legalDoc.create({
        data: { ...doc, publishedAt: new Date() },
      });
      console.log(`  ✓ legal "${doc.slug}@${doc.version}" created`);
    }
  }
}
